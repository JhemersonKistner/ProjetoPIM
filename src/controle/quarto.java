public class quarto {
    private int numquarto;
    private int andar_quarto;
    private int num_camas;
    private String varanda;

    public int getNumquarto() {
        return numquarto;
    }

    public void setNumquarto(int numquarto) {
        this.numquarto = numquarto;
    }

    public int getAndar_quarto() {
        return andar_quarto;
    }

    public void setAndar_quarto(int andar_quarto) {
        this.andar_quarto = andar_quarto;
    }

    public int getNum_camas() {
        return num_camas;
    }

    public void setNum_camas(int num_camas) {
        this.num_camas = num_camas;
    }

    public String getVaranda() {
        return varanda;
    }

    public void setVaranda(String varanda) {
        this.varanda = varanda;
    }
}
